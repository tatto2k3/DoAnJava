    private void initTable(){
        tblModel = new DefaultTableModel();
        tblModel.setColumnIdentifiers(new String[]{"Mã nhà cung cấp","Tên nhà cung cấp","Địa chỉ", "Email","Số điện thoại"});
        tableNCC.setModel(tblModel);
    }
    
    private void loadDataToTable(){
        try{
            prNhaCungCap kt = new prNhaCungCap();
            List<NhaCungCap> list = kt.FindAll();
            tblModel.setRowCount(0);
            for(NhaCungCap it :list){
                tblModel.addRow(new Object[]{
                it.getMaNCC(), it.getTenNCC(), it.getDiachi(), it.getEmail(), 
                it.getSDT()
                });
            }
            tblModel.fireTableDataChanged();
        }catch(Exception e){
            e.printStackTrace();
            ThongBao.showLoi(parentForm, e.getMessage(), "Lỗi");
        }
    }
    
    private String getmaKM(){
        int count =0;
        prNhaCungCap kt = new prNhaCungCap();
        try {
            count = kt.somaNCC();
        } catch (Exception ex) {
            Logger.getLogger(QuanLyBanHang.class.getName()).log(Level.SEVERE, null, ex);
        }
        if(count<9){
            return "NCC0" +(count+1);
        }else if(count>=9 && count<99){
            return "NCC" +(count+1);
        }
        return null;
    }

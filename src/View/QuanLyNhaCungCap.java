    private void tableNCCMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_tableNCCMouseClicked
        try{
            int row = tableNCC.getSelectedRow();
            
            if(row>=0){
                String id = (String) tableNCC.getValueAt(row, 0);
                prNhaCungCap kt = new prNhaCungCap();
                NhaCungCap ncc = kt.FindByID(id);
                
                if(ncc != null){
                    txtMaNCC.setText(ncc.getMaNCC());
                    txtTenNCC.setText(ncc.getTenNCC());
                    txtDiachi.setText(ncc.getDiachi());
                    txtEmail.setText(ncc.getEmail());
                    txtSDT.setText(ncc.getSDT());
                }
            }
        }catch(Exception e){
            e.printStackTrace();
            ThongBao.showLoi(parentForm, e.getMessage(), "Lỗi");
        }
    }//GEN-LAST:event_tableNCCMouseClicked


    private void btnXoaNCCActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnXoaNCCActionPerformed
        StringBuilder sb =new StringBuilder();
        KiemTraDuLieu.DuLieuTrong(txtMaNCC, sb, "Mã nhà cung cấp trống!!");
        if(sb.length()>0){
            ThongBao.showLoi(parentForm, sb.toString(), "Lỗi!!");
            return;
        }
        if(ThongBao.showXacNhan(parentForm, "Bạn có muốn xóa nhà cung cấp không?", "Hỏi")== JOptionPane.NO_OPTION){
            return;
        }
        try{
            prNhaCungCap kt = new prNhaCungCap();
            if(kt.delete(txtMaNCC.getText())){
                ThongBao.showThongBao(parentForm, "Nhà cung cấp đã được xóa", "Thông báo");
                btnThemNCCActionPerformed(evt);
                loadDataToTable();
            }else{
                ThongBao.showXacNhan(parentForm, "Lỗi!! Không thể xóa.", "Cảnh báo");
            }
            
        }catch(Exception e){
            e.printStackTrace();
            ThongBao.showLoi(parentForm, e.getMessage(), "Lỗi");
        }
    }//GEN-LAST:event_btnXoaNCCActionPerformed

    private void btnLuuNCCActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnLuuNCCActionPerformed
        StringBuilder sb =new StringBuilder();
        KiemTraDuLieu.DuLieuTrong(txtMaNCC, sb, "Mã nhà cung cấp trống!!");
        KiemTraDuLieu.DuLieuTrong(txtTenNCC, sb, "Tên nhà cung cấp trống!!");
        if(sb.length()>0){
            ThongBao.showLoi(parentForm, sb.toString(), "Lỗi!!");
            return;
        }
        try{
            NhaCungCap ncc =new NhaCungCap();
            ncc.setMaNCC(txtMaNCC.getText());
            ncc.setTenNCC(txtTenNCC.getText());
            ncc.setDiachi(txtDiachi.getText());
            ncc.setEmail(txtEmail.getText());
            ncc.setSDT(txtSDT.getText());
            
            prNhaCungCap kt = new prNhaCungCap();
            if(ncc.getSDT().length()<10 || ncc.getSDT().length()>11){
                ThongBao.showLoi(parentForm, "Lỗi số điện thoại", "Lỗi");
            }
            else if(kt.insert(ncc)){
                ThongBao.showThongBao(parentForm, "Nhà cung cấp đã được lưu", "Thông báo");
                loadDataToTable();
            }else{
                ThongBao.showXacNhan(parentForm, "Lỗi!! Không thể lưu.", "Cảnh báo");
            }
            
        }catch(Exception e){
            e.printStackTrace();
            ThongBao.showLoi(parentForm, e.getMessage(), "Lỗi");
        }
    }//GEN-LAST:event_btnLuuNCCActionPerformed


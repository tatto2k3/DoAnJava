    private String getsoHD(){
        int count =0;
        prBanHang kt = new prBanHang();
        try {
            count = kt.soHD();
        } catch (Exception ex) {
            Logger.getLogger(QuanLyBanHang.class.getName()).log(Level.SEVERE, null, ex);
        }
        if(count<9){
            return "HD" + 00 +(count+1);
        }else if(count>=9 && count<99){
            return "HD" + 0 +(count+1);
        }else
            return "HD"  +(count+1);
    }
    

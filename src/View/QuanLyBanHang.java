    public void XuatHoaDon(String sohd){
        try{
            Hashtable map = new Hashtable();
            JasperReport report = JasperCompileManager.compileReport("src//Resource//reportHoaDon.jrxml");
            
            map.put("SoHD", sohd);
            
            JasperPrint p = JasperFillManager.fillReport(report, map, KetNoiDatabase.getOracleConnection());
            
            JasperViewer.viewReport(p, false);
        }catch(Exception ex){
            System.out.println(ex.getMessage());
        }
    }


    private void loadDataToTable(){
        try{
            prSanPham kt = new prSanPham();
            List<SanPham> list = kt.FindAll();
            tblModel1.setRowCount(0);
            tblModel2.setRowCount(0);
            for(SanPham it :list){
                tblModel1.addRow(new Object[]{
                it.getMaSP(), it.getTenSP(), it.getMauSac(), it.getDongia(), it.getSoluong()
                });
            }
            tblModel1.fireTableDataChanged();
            txttongTien.setText("0");
        }catch(Exception e){
            e.printStackTrace();
            ThongBao.showLoi(parentForm, e.getMessage(), "Lỗi");
        }
    }
